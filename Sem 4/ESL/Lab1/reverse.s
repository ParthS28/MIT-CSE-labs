	DCD 0x10001000
	DCD Reset_Handler	
	AREA MyCode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=SRC
	LDR R1,=DST
	ADD R0,R0,#36
	MOV R3,#10
UP	LDR R2,[R0],#-4
	STR R2,[R1],#4
	SUBS R3,#1
	BNE UP
STOP 
	B STOP
SRC DCD 0x1,0x2,0x3,0x4,0x5,0x6,0x7,0x8,0x9,0xA
	AREA Mydata,DATA,READWRITE
DST DCD 0
	END
